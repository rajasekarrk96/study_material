# 1. Bar and Column Charts
# Usage: Bar and column charts are ideal for comparing quantities across different categories.
#        Bar charts display data with horizontal bars, while column charts use vertical bars.
# Types:
# - Clustered Bar/Column Chart: Compares multiple categories side by side.
# - Stacked Bar/Column Chart: Shows the contribution of each category to the total.
# - 100% Stacked Bar/Column Chart: Displays relative percentage contributions.

# 2. Line Chart
# Usage: Line charts are used to track changes over periods of time. They are ideal for showing trends and data patterns.
# Types:
# - Standard Line Chart: Simple line connecting data points over time.
# - Multiple Line Chart: Multiple lines for comparing different data sets over the same period.

# 3. Area Chart
# Usage: Area charts are similar to line charts but with the area below the line filled in. They are used to show volume over time.
# Types:
# - Standard Area Chart: Shows the magnitude of trends.
# - Stacked Area Chart: Displays the contribution of each part to the whole over time.

# 4. Pie and Donut Charts
# Usage: Pie and donut charts are used to show proportions of a whole. They are best for illustrating parts of a whole with a limited number of categories.
# Types:
# - Pie Chart: A circular chart divided into slices.
# - Donut Chart: Similar to a pie chart but with a blank center.

# 5. Scatter and Bubble Charts
# Usage: Scatter charts show the relationship between two numerical values. Bubble charts are an extension of scatter charts with an additional dimension represented by the size of the bubble.
# Types:
# - Scatter Chart: Plots points based on two variables.
# - Bubble Chart: Adds a third variable through the size of bubbles.

# 6. Histogram
# Usage: Histograms are used to show the distribution of a single variable. They are useful for understanding the frequency distribution of data.
# Types:
# - Standard Histogram: Displays the frequency of data points in specific ranges.

# 7. Box Plot
# Usage: Box plots (also known as box-and-whisker plots) display the distribution of data based on a five-number summary (minimum, first quartile, median, third quartile, and maximum). They are useful for identifying outliers and understanding data distribution.
# Types:
# - Standard Box Plot: Shows distribution of data with quartiles and median.

# 8. Density Plot
# Usage: Density plots show the distribution of a continuous variable, smoothed to visualize the data's density. They are useful for understanding the distribution pattern of data.
# Types:
# - Standard Density Plot: Displays the distribution and density of data.

# 9. Violin Plot
# Usage: Violin plots show the distribution of data across different categories. They combine aspects of box plots and density plots.
# Types:
# - Standard Violin Plot: Displays data distribution and density.

# 10. Waterfall Chart
# Usage: Waterfall charts are used to show the cumulative effect of sequential positive and negative values. They are great for visualizing changes in financial metrics.
# Types:
# - Standard Waterfall Chart: Illustrates how an initial value is affected by a series of intermediate positive or negative values.

# 11. Funnel Chart
# Usage: Funnel charts are used to show a process that involves stages. They are particularly useful in sales and marketing for showing conversion rates.
# Types:
# - Standard Funnel Chart: Represents stages in a process and the proportion of data at each stage.

# 12. Gauge Chart
# Usage: Gauge charts display progress towards a goal. They are used to measure performance against a target.
# Types:
# - Standard Gauge Chart: Shows current status relative to a target value.

# 13. Radial Gauge
# Usage: Radial gauges are similar to gauge charts but in a radial form. They are used to show progress towards a goal.
# Types:
# - Standard Radial Gauge: Displays current status relative to a target in a radial format.

# 14. Map Visuals
# Usage: Map visuals are used to display geographical data. They are useful for showing data distribution across locations.
# Types:
# - Map: Basic geographical map.
# - Filled Map: Displays areas filled based on values.
# - ArcGIS Map: Advanced mapping features.

# 15. Tree Map
# Usage: Tree maps display hierarchical data as nested rectangles. They are ideal for showing proportions within a hierarchy.
# Types:
# - Standard Tree Map: Uses color and size to represent data dimensions.

# 16. Matrix and Table
# Usage: Matrices and tables are used to display detailed data in rows and columns. They are best for showing precise values and for detailed analysis.
# Types:
# - Matrix: Allows for displaying data with multiple levels of row and column groupings.
# - Table: Simple tabular format for showing data in a grid.

# 17. KPI (Key Performance Indicator)
# Usage: KPI visuals are used to measure progress toward a goal. They highlight how actual performance compares to target values.
# Types:
# - Standard KPI: Shows a key value, target value, and progress indicator.

# 18. Slicer
# Usage: Slicers are interactive filtering controls. They allow users to slice and dice data dynamically.
# Types:
# - Standard Slicer: Provides interactive filtering for other visuals.
# - Date Slicer: Specialized for filtering data by date.

# 19. Card
# Usage: Cards are used to display single numbers. They are ideal for highlighting key metrics.
# Types:
# - Single Number Card: Shows a single value.
# - Multi-row Card: Displays multiple values in a list.

# 20. Combo Chart
# Usage: Combo charts combine a line chart and a bar chart to visualize two different data sets together. They are useful for showing relationships between different types of data.
# Types:
# - Line and Stacked Column Chart: Combines line and stacked column charts.
# - Line and Clustered Column Chart: Combines line and clustered column charts.

# 21. Heat Map
# Usage: Heat maps use color to show the density or intensity of data points in a geographical area or a matrix. They are effective for identifying patterns and trends.
# Types:
# - Heat Map: Displays data density using color gradients.

# 22. Bullet Chart
# Usage: Bullet charts are used to compare a measure against a target or a range of values. They are effective for performance tracking.
# Types:
# - Standard Bullet Chart: Displays a single measure against a target range.

# 23. Ribbon Chart
# Usage: Ribbon charts show rank changes over time, highlighting the flow of data across different categories. They are useful for comparing rank changes and trends.
# Types:
# - Standard Ribbon Chart: Illustrates rank changes with ribbons connecting different categories.

# 24. Decomposition Tree
# Usage: Decomposition trees are used to break down a measure into its contributing factors. They are great for root cause analysis.
# Types:
# - Standard Decomposition Tree: Visualizes hierarchical data decomposition.

# 25. Key Influencers
# Usage: Key Influencers visual helps identify factors that influence a particular metric. It is used for predictive analytics and understanding data drivers.
# Types:
# - Key Influencers Visual: Analyzes data to determine key influencers for a selected metric.

# 26. Scatter Map
# Usage: Scatter maps combine scatter charts with geographical maps to show data points on a map. They are useful for geospatial analysis.
# Types:
# - Scatter Map: Plots data points on a geographical map.

# 27. Sparkline
# Usage: Sparklines are small, simple charts embedded within a table cell to show trends. They are used for quick trend analysis in tabular data.
# Types:
# - Standard Sparkline: Embedded line chart within table cells.

# 28. Treemap
# Usage: Treemaps display hierarchical data as a set of nested rectangles. They are used for showing proportions within a hierarchy.
# Types:
# - Standard Treemap: Uses color and size to represent data dimensions.

# 29. Choropleth Map
# Usage: Choropleth maps use varying shades of color to represent data values in different geographical areas. They are useful for showing density or intensity of data across regions.
# Types:
# - Choropleth Map: Color-coded map based on data values.

# 30. Sunburst Chart
# Usage: Sunburst charts display hierarchical data through concentric circles. They are used to show part-to-whole relationships.
# Types:
# - Standard Sunburst Chart: Visualizes hierarchical data with concentric rings.

# 31. Word Cloud
# Usage: Word clouds represent text data where the size of each word indicates its frequency or importance. They are useful for text analysis.
# Types:
# - Standard Word Cloud: Visualizes the frequency of words in a text dataset.

# 32. Custom Visuals
# Usage: Power BI supports custom visuals created by the community or third-party developers. These visuals provide additional functionality beyond the built-in options.
# Types:
# - Various: Custom visuals can range from advanced chart types to specialized data representations.
