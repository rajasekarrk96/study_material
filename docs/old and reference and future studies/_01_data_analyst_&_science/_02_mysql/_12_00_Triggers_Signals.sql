USE student;

/*
--------------------------------------------------------------------------------
1. TRIGGER BASICS
--------------------------------------------------------------------------------
- Trigger is a stored program that runs automatically on table events
- Events: INSERT, UPDATE, DELETE
- Timing: BEFORE or AFTER
--------------------------------------------------------------------------------
*/

/*
--------------------------------------------------------------------------------
2. NEW AND OLD
--------------------------------------------------------------------------------
NEW:
- new row values in INSERT or UPDATE

OLD:
- existing row values in UPDATE or DELETE
--------------------------------------------------------------------------------
*/

/*
--------------------------------------------------------------------------------
3. BEFORE INSERT TRIGGER
--------------------------------------------------------------------------------
- Common for validation or data cleanup before row is stored
--------------------------------------------------------------------------------
*/

/*
--------------------------------------------------------------------------------
4. AFTER INSERT TRIGGER
--------------------------------------------------------------------------------
- Common for logging or audit tables
--------------------------------------------------------------------------------
*/

/*
--------------------------------------------------------------------------------
5. BEFORE UPDATE TRIGGER
--------------------------------------------------------------------------------
- Validate changes before update is accepted
--------------------------------------------------------------------------------
*/

/*
--------------------------------------------------------------------------------
6. AFTER UPDATE TRIGGER
--------------------------------------------------------------------------------
- Audit what changed
--------------------------------------------------------------------------------
*/

/*
--------------------------------------------------------------------------------
7. BEFORE DELETE / AFTER DELETE
--------------------------------------------------------------------------------
- Use for archive or totals adjustment logic
--------------------------------------------------------------------------------
*/

/*
--------------------------------------------------------------------------------
8. SIGNAL
--------------------------------------------------------------------------------
- SIGNAL SQLSTATE '45000' throws custom application error
- Used to reject invalid business rules
--------------------------------------------------------------------------------
*/

/*
--------------------------------------------------------------------------------
9. SHOW AND DROP TRIGGERS
--------------------------------------------------------------------------------
- SHOW TRIGGERS
- DROP TRIGGER trigger_name
--------------------------------------------------------------------------------
*/

/*
--------------------------------------------------------------------------------
10. COMMON MISTAKES
--------------------------------------------------------------------------------
1. Forgetting FOR EACH ROW
2. Misusing OLD in INSERT trigger
3. Misusing NEW in DELETE trigger
4. Writing triggers that create unintended side effects
--------------------------------------------------------------------------------
*/

/*
================================================================================
SUMMARY
================================================================================
- Triggers automate event-based logic
- BEFORE triggers validate or adjust data
- AFTER triggers log or respond after data change
- SIGNAL blocks invalid operations with custom errors
================================================================================
*/
