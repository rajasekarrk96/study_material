def main() -> None:
    parser = argparse.ArgumentParser(description="Generate prompt engineering note notebooks.")
    parser.add_argument(
        "--module",
        help="Generate only one module by folder name, for example 01_foundations",
    )
    args = parser.parse_args()

    OUTPUT_DIR.mkdir(parents=True, exist_ok=True)
    write_readme(COURSE)
    write_notebook(OUTPUT_DIR / "00_course_map" / "00_overview.ipynb", build_overview_notebook(COURSE))

    selected_modules = COURSE
    if args.module:
        selected_modules = [module for module in COURSE if module["folder"] == args.module]
        if not selected_modules:
            raise SystemExit(f"Unknown module: {args.module}")

    for module in selected_modules:
        module_dir = OUTPUT_DIR / module["folder"]
        module_dir.mkdir(parents=True, exist_ok=True)
        for index, lesson in enumerate(module["lessons"], start=1):
            filename = f"{index:02d}_{slugify(lesson['title'])}.ipynb"
            notebook = build_notebook(module, lesson)
            write_notebook(module_dir / filename, notebook)

    if args.module:
        print(f"Generated module {args.module} under: {OUTPUT_DIR}")
    else:
        print(f"Generated notebooks under: {OUTPUT_DIR}")


if __name__ == "__main__":
    main()
