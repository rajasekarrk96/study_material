ROOT = Path(__file__).resolve().parent
OUTPUT_DIR = ROOT / "prompt_engineering_notes"


def slugify(text: str) -> str:
    text = text.lower()
    text = re.sub(r"[^a-z0-9]+", "_", text)
    return text.strip("_")


def lines_from_list(items: list[str], prefix: str = "- ") -> list[str]:
    return [f"{prefix}{item}\n" for item in items]


def md_cell(text: str) -> dict:
    if not text.endswith("\n"):
        text += "\n"
    return {
        "cell_type": "markdown",
        "metadata": {},
        "source": text.splitlines(keepends=True),
    }


def notebook_metadata() -> dict:
    return {
        "kernelspec": {
            "display_name": "Python 3",
            "language": "python",
            "name": "python3",
        },
        "language_info": {
            "name": "python",
            "version": "",
        },
    }


def build_notebook(module: dict, lesson: dict) -> dict:
    title = lesson["title"]
    focus = lesson["focus"]
    level = lesson["level"]
    objectives = lesson["objectives"]
    microtopics = lesson["microtopics"]
    notes = lesson["notes"]
    frameworks = lesson["frameworks"]
    examples = lesson["examples"]
    mistakes = lesson["mistakes"]
    evaluation = lesson["evaluation"]
    practice = lesson["practice"]

    cells = [
        md_cell(
            f"# {title}\n\n"
            f"**Module:** {module['title']}\n\n"
            f"**Level:** {level}\n\n"
            f"**Topic Focus:** {focus}\n"
        ),
        md_cell(
            "## Learning Objectives\n\n" + "".join(lines_from_list(objectives))
        ),
        md_cell(
            "## Why This Topic Matters\n\n"
            f"{lesson['why_it_matters']}\n\n"
            "Prompt engineering improves when you can identify the exact job a prompt must do, the failure modes it creates, and the levers available to improve it.\n"
        ),
        md_cell(
            "## Small Topics Inside This Topic\n\n"
            "Study this notebook from the smallest ideas upward:\n\n"
            + "".join(lines_from_list(microtopics))
        ),
        md_cell(
            "## Core Notes\n\n"
            + "".join([f"### {section['heading']}\n\n{section['body']}\n\n" for section in notes])
        ),
        md_cell(
            "## Frameworks And Design Patterns\n\n"
            + "".join(
                [
                    f"### {item['name']}\n\n"
                    f"**Use when:** {item['use_when']}\n\n"
                    f"**How it works:** {item['how_it_works']}\n\n"
                    f"**Watch out for:** {item['watch_out_for']}\n\n"
                    for item in frameworks
                ]
            )
        ),
        md_cell(
            "## Prompt Examples\n\n"
            + "".join(
                [
                    f"### {example['label']}\n\n"
                    f"**Scenario:** {example['scenario']}\n\n"
                    "```text\n"
                    f"{example['prompt']}\n"
                    "```\n\n"
                    f"**Why this example matters:** {example['why']}\n\n"
                    for example in examples
                ]
            )
        ),
        md_cell(
            "## Common Mistakes And Corrections\n\n"
            + "".join(
                [
                    f"### Mistake {idx}\n\n"
                    f"**Problem:** {item['problem']}\n\n"
                    f"**Why it hurts:** {item['impact']}\n\n"
                    f"**Better move:** {item['fix']}\n\n"
                    for idx, item in enumerate(mistakes, start=1)
                ]
            )
        ),
        md_cell(
            "## Evaluation Checklist\n\n" + "".join(lines_from_list(evaluation))
        ),
        md_cell(
            "## Practice Prompts\n\n"
            "Use these as writing drills, not as code exercises.\n\n"
            + "".join([f"{idx}. {item}\n" for idx, item in enumerate(practice, start=1)])
        ),
        md_cell(
            "## Summary\n\n"
            f"{lesson['summary']}\n\n"
            "A strong prompt engineer can explain the topic simply, apply it deliberately, and diagnose when it is missing from a workflow.\n"
        ),
    ]

    return {
        "cells": cells,
        "metadata": notebook_metadata(),
        "nbformat": 4,
        "nbformat_minor": 5,
    }


def write_notebook(path: Path, notebook: dict) -> None:
    path.parent.mkdir(parents=True, exist_ok=True)
    path.write_text(json.dumps(notebook, indent=1, ensure_ascii=True) + "\n", encoding="utf-8")


def write_readme(course: list[dict]) -> None:
    lines = [
        "# Prompt Engineering Notes\n\n",
        "This folder is generated by `generate.py`.\n\n",
        "The goal is to provide notebook-style notes from fundamentals to expert-level prompt systems thinking.\n\n",
        "## Modules\n\n",
    ]
    for module in course:
        lines.append(f"- `{module['folder']}` - {module['title']}\n")
    lines.extend(
        [
            "\n## How To Use\n\n",
            "1. Study modules in order.\n",
            "2. In each notebook, move from small topics to larger frameworks.\n",
            "3. Rewrite the sample prompts in your own words.\n",
            "4. Keep a separate experiment notebook for your own prompt tests.\n",
            "5. Revisit the evaluation, safety, and iteration modules repeatedly.\n",
        ]
    )
    (OUTPUT_DIR / "README.md").write_text("".join(lines), encoding="utf-8")


def build_overview_notebook(course: list[dict]) -> dict:
    module_lines = []
    for module in course:
        module_lines.append(f"## {module['folder']} - {module['title']}\n\n")
        module_lines.extend(lines_from_list([lesson["title"] for lesson in module["lessons"]]))
        module_lines.append("\n")

    cells = [
        md_cell(
            "# Prompt Engineering Course Map\n\n"
            "This notebook lists the full curriculum generated by `generate.py`.\n"
        ),
        md_cell(
            "## Study Philosophy\n\n"
            "- Learn prompt engineering as a system, not as a bag of tricks.\n"
            "- Move from clarity and structure to evaluation, safety, retrieval, tool use, and prompt operations.\n"
            "- Reuse the prompt patterns, but always adapt them to task, model, and constraints.\n"
        ),
        md_cell("## Full Curriculum\n\n" + "".join(module_lines)),
    ]
    return {
        "cells": cells,
        "metadata": notebook_metadata(),
        "nbformat": 4,
        "nbformat_minor": 5,
    }


